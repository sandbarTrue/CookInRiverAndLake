public class Menu {
    private String name;
    private Integer fry;
    private Integer toast;
    private Integer boil;
    private Integer steamed;
    private Integer zha;
    private Integer slice;
    private String  level;
    private Integer amount;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getFry() {
        return fry;
    }

    public void setFry(Integer fry) {
        this.fry = fry;
    }

    public Integer getToast() {
        return toast;
    }

    public void setToast(Integer toast) {
        this.toast = toast;
    }

    public Integer getBoil() {
        return boil;
    }

    public void setBoil(Integer boil) {
        this.boil = boil;
    }

    public Integer getSteamed() {
        return steamed;
    }

    public void setSteamed(Integer steamed) {
        this.steamed = steamed;
    }

    public Integer getZha() {
        return zha;
    }

    public void setZha(Integer zha) {
        this.zha = zha;
    }

    public Integer getSlice() {
        return slice;
    }

    public void setSlice(Integer slice) {
        this.slice = slice;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public Integer getAmount() {
        return amount;
    }

    public void setAmount(Integer amount) {
        this.amount = amount;
    }
}
